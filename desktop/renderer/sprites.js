var RARITY_COLORS = {
  common:    '#aaaaaa',
  uncommon:  '#4caf50',
  rare:      '#42a5f5',
  epic:      '#ba68c8',
  legendary: '#ffd54f',
};

// 渲染精灵，返回字符串数组（每行一条）
function renderSprite(bones, frame) {
  frame = frame || 0;
  var frames = BODIES[bones.species] || BODIES['blob'];
  var body = frames[frame % frames.length].map(function(line) {
    return line.split('{E}').join(bones.eye);
  });
  var lines = body.slice();
  // 帽子渲染在第0行（如果该行为空）
  if (bones.hat && bones.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HAT_LINES[bones.hat] || '';
  }
  // 如果第0行全空且所有帧第0行都空，去掉
  if (!lines[0].trim()) {
    var allEmpty = frames.every(function(f) { return !f[0].trim(); });
    if (allEmpty) lines = lines.slice(1);
  }
  return lines;
}
